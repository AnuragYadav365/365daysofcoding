    {

        int bal = 0;
        int ans = 0;

        for (int i = 0; i < s.length(); ++i) {

            bal += s.charAt(i) == '(' ? 1 : -1;

            if (bal == -1) {
                ans += 1;
                bal += 1;
            }
        }

        return bal + ans;
    }

